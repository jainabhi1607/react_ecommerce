import mongoose from "mongoose";
const AuthSchema = new mongoose.Schema({
name: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
  },
},{timestamps: true});

export const AuthModel = mongoose.model("auth", AuthSchema);